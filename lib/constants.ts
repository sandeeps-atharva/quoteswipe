// Default background image (none)
export const DEFAULT_BACKGROUND: BackgroundImage = BACKGROUND_IMAGES[0];

/**
 * Get a consistent "random" background image based on quote ID
 * Same quote ID always returns the same background
 * Only uses actual images (excludes 'none' option)
 */
export function getRandomBackgroundForQuote(quoteId: string | number): BackgroundImage {
  // Filter to only get actual background images (exclude 'none')
  const actualBackgrounds = BACKGROUND_IMAGES.filter(bg => bg.id !== 'none' && bg.url);
  
  if (actualBackgrounds.length === 0) {
    return DEFAULT_BACKGROUND;
  }
  
  const idStr = String(quoteId);
  // Create a simple hash from the quote ID
  let hash = 0;
  for (let i = 0; i < idStr.length; i++) {
    const char = idStr.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32-bit integer
  }
  // Use absolute value and modulo to get index
  const index = Math.abs(hash) % actualBackgrounds.length;
  return actualBackgrounds[index];
}

/**
 * Get a completely random background image (for "Random" button)
 */
export function getRandomBackground(): BackgroundImage {
  const actualBackgrounds = BACKGROUND_IMAGES.filter(bg => bg.id !== 'none' && bg.url);
  if (actualBackgrounds.length === 0) {
    return DEFAULT_BACKGROUND;
  }
  const randomIndex = Math.floor(Math.random() * actualBackgrounds.length);
  return actualBackgrounds[randomIndex];
}

